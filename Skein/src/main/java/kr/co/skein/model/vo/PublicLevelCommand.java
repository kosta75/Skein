package kr.co.skein.model.vo;

public class PublicLevelCommand {
	
	private int publicLevelCode;
	private String publicLevelName;
	private String publicLevelDescription;
	
	public int getPublicLevelCode() {
		return publicLevelCode;
	}
	public void setPublicLevelCode(int publicLevelCode) {
		this.publicLevelCode = publicLevelCode;
	}
	public String getPublicLevelName() {
		return publicLevelName;
	}
	public void setPublicLevelName(String publicLevelName) {
		this.publicLevelName = publicLevelName;
	}
	public String getPublicLevelDescription() {
		return publicLevelDescription;
	}
	public void setPublicLevelDescription(String publicLevelDescription) {
		this.publicLevelDescription = publicLevelDescription;
	}
	
	
}
