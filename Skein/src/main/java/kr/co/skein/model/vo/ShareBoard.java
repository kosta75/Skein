package kr.co.skein.model.vo;

public class ShareBoard {

	private String shareSeq;
	private String shareEmail;
	private String boardSeq;
	private String sourceBoardSeq;
	public String getShareSeq() {
		return shareSeq;
	}
	public void setShareSeq(String shareSeq) {
		this.shareSeq = shareSeq;
	}
	public String getShareEmail() {
		return shareEmail;
	}
	public void setShareEmail(String shareEmail) {
		this.shareEmail = shareEmail;
	}
	public String getBoardSeq() {
		return boardSeq;
	}
	public void setBoardSeq(String boardSeq) {
		this.boardSeq = boardSeq;
	}
	public String getSourceBoardSeq() {
		return sourceBoardSeq;
	}
	public void setSourceBoardSeq(String sourceBoardSeq) {
		this.sourceBoardSeq = sourceBoardSeq;
	}
	
	
}
